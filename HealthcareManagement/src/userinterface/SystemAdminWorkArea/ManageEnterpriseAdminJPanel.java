    private void updateEnterpriseJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateEnterpriseJButtonActionPerformed
        
        int row = enterpriseJTable.getSelectedRow();
        UserAccount ua;
        
        String username;
        String enterpriseName;
        String networkName;
        
        if (row < 0) 
            return;
        
        enterpriseName = enterpriseJTable.getValueAt(row, 0).toString();
        username = enterpriseJTable.getValueAt(row,1).toString();
        
        ua = this.system.searchNetwork("System").getEnterpriseDirectory().searchEnterprise(enterpriseName).getUserDirectory().searchUser(username);
        
        JFrame regFrame = new JFrame();
        UpdateAdminDtlsJPanel panel = new UpdateAdminDtlsJPanel(ua, this);
        
        regFrame.getContentPane().add(panel);
        regFrame.setExtendedState(javax.swing.JFrame.MAXIMIZED_BOTH); // Window opens in full size mode
        regFrame.setVisible(true);                
    }//GEN-LAST:event_updateEnterpriseJButtonActionPerformed

